var gDetails = document.querySelectorAll('.g-detail');
var len = gDetails.length;
for(var i =0; i<len; i++) {
    var $curDetail = gDetails[i];
    $curDetail.addEventListener('click', function(e) {
        e.preventDefault();
        var $target = e.target;
        var content = gallery[this.getAttribute('data-id')];
        var curIndex = this.getAttribute('data-index');
        if(typeof curIndex === 'undefined' || null === curIndex) {
            curIndex = -1;
        }
        curIndex = parseInt(curIndex, 10);
        var isLeft = !$target.classList.contains('after');
        if(isLeft) {
            curIndex -= 1;
            if(curIndex < 0) {
                curIndex = content.length - 1;
            }
        } else {
            curIndex += 1;
            if(curIndex >= content.length) {
                curIndex = 0;
            }
        }
        var $img = this.querySelector('img');
        $img.src = content[curIndex];
        this.setAttribute('data-index', curIndex);
    });
}